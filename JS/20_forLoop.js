/* 
FOR LOOP - repeat some code a
certain amount of times
*/

// TO COUNT FROM 1 TO 100 OR YOUR GIVEN NUMBER

for (let counter = 1; counter <= 100; counter += 1) {
  console.log(counter);
}

// TO COUNTDOWN FROM 10 TO 1

for (let i = 10; i > 0; i -= 1) {
  console.log(i);
}
console.log("HAPPY NEW YEAR");

for (m = 30; m > 0; m -= 1) {
  console.log(m);
}
console.log("TIME UP!");

for (time = 120; time >= 60; time -= 1) {
  console.log(time);
}
console.log("60 Seconds Gone!");
for (time = 59; time >= 0; time -= 1) {
  console.log(time);
}
